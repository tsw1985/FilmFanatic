package dad.filmfanatic.items;

public class CountryItem {
	private String iso;
	private String name;

	public String getIso() {
		return iso;
	}

	public void setIso(String iso) {
		this.iso = iso;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return iso;
	}

}
